    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ModelTabeleTrening mtt = (ModelTabeleTrening) jTableTreninzi.getModel();
        List<Trening> treninzi = mtt.vratiTreninge();
        try {
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setAkcija(Operacija.SACUVAJ_LISTU_ODO);
            kz.setParametar(treninzi);
            KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
            ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
            if (so.getStatus() == StatusOdgovora.OK) {
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio treninge", "Obaveštenje", JOptionPane.INFORMATION_MESSAGE);
                resetujFormu();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti treninge", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Trener t = (Trener) jComboBoxTrener.getSelectedItem();
        Clan c = (Clan) jComboBoxIgrac.getSelectedItem();
        String dat = jTextFieldDatum.getText().trim();
        VrstaTreninga vt = (VrstaTreninga) jComboBoxVrstaTreninga.getSelectedItem();
        String duzTrajanja = jTextFieldDuzinaTrajanja.getText().trim();
        String cilj = jTextFieldICilj.getText().trim();
        String sadrzaj = jTextAreaSadrzaj.getText().trim();
        if (dat.isEmpty() || duzTrajanja.isEmpty() || cilj.isEmpty() || sadrzaj.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate popuniti sva polja", "Upozorenje", JOptionPane.WARNING_MESSAGE);
            return;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        Date datum = null;
        try {
            datum = sdf.parse(dat);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Datum morate uneti u formatu dd.MM.yyyy", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int duzinaTrajanja = 0;
        try {
            duzinaTrajanja = Integer.parseInt(duzTrajanja);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "U polje dužina trajanja morate uneti broj", "Upozorenje", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Trening trening = new Trening();
        trening.setTrener(t);
        trening.setClan(c);
        trening.setDatum(datum);
        trening.setDuzinaTrajanja(duzinaTrajanja);
        trening.setCilj(cilj);
        trening.setSadrzaj(sadrzaj);
        trening.setVrstaTreninga(vt);
        ModelTabeleTrening mtt = (ModelTabeleTrening) jTableTreninzi.getModel();
        mtt.dodaj(trening);
        jTextFieldDatum.setText("");
        jComboBoxVrstaTreninga.setSelectedIndex(0);
        jTextFieldDuzinaTrajanja.setText("");
        jTextFieldICilj.setText("");
        jTextAreaSadrzaj.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

