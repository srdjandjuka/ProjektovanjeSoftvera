    private void srediFormu() {
        ModelTabeleTrening mtt = new ModelTabeleTrening();
        jTableTreninzi.setModel(mtt);
        srediKoloneTabele();
    }

    private void popuniComboVrstaTreninga() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setAkcija(Operacija.VRATI_SVE_ZA_ODO);
        kz.setParametar(new VrstaTreninga());

        try {
            KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
            ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
            jComboBoxVrstaTreninga.removeAllItems();
            if (so.getStatus() == StatusOdgovora.OK) {
                List<VrstaTreninga> lista = (List<VrstaTreninga>) so.getOdgovor();
                for (VrstaTreninga vrstaTreninga : lista) {
                    jComboBoxVrstaTreninga.addItem(vrstaTreninga);
                }
            }

        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(FClan.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void popuniComboTrener() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setAkcija(Operacija.VRATI_SVE_ZA_ODO);
        kz.setParametar(new Trener());
        try {
            KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
            ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
            jComboBoxTrener.removeAllItems();

            if (so.getStatus() == StatusOdgovora.OK) {

                List<Trener> lista = (List<Trener>) so.getOdgovor();
                for (Trener trener : lista) {
                    jComboBoxTrener.addItem(trener);
                }
            }

        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(FClan.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

